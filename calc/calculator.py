class Calculator:

    def __init__(self, no1, no2):
        self.no1 = no1
        self.no2 = no2

    def sum(self):
        return self.no1 + self.no2

    def mul(self):
        return self.no1 * self.no2

    def sub(self):
        return self.no1 - self.no2

    def div(self):
        return self.no1 / self.no2